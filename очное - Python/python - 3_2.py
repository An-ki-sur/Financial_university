print('------------ Task 1 ------------')
def t1(x, a):
    if x == 1:
        return a
    if x == 2:
        return -a[-1] / a[-2]
    if x == 3:
        d = a[1] ** 2 - 4 * a[0] * a[2]
        if d < 0:
            return 'None'
        x1 = (-a[1] + d ** 0.5) / 2 * a[0]
        x2 = (-a[1] - d ** 0.5) / 2 * a[0]
        return x1, x2
    return 'None'


x = int(input())
a = [int(input()) for i in range(x)]
print(t1(x, a))


